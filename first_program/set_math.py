math_students = {"Matthew", "Helen", "Prashant", "James", "Arpana"}
biology_students = {"Jane", "Matthew", "Charlotte", "Mesut", "Oliver", "James"}

# Union example - gives a unique set of elements when accounting for all elements in both/multiple sets. No duplicates

print(math_students | biology_students)

# Intersection example - gives unique set of elements that appear in multiple sets

print(math_students & biology_students)
